package com.codexsoft.yormoney.domain;

public enum Roles {
    Free("Free"),
    RegRegular("RegRegular"),
    RegPremium("RegPremium"),
    Regular("Regular"),
    Premium("Premium"),
    Admin("Administrator");

    private String name;

    Roles(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public  String toString(){
        return getName();
    }
}
