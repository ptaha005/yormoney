package com.codexsoft.yormoney.util;

import java.util.ArrayList;
import java.util.List;

public class CategoryTotal{
    private String name;
    private Long price = 0L;

    private List<ExpenditureTotal> items = new ArrayList<ExpenditureTotal>();

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPrice() {
        return price;
    }

    public void setPrice(Long price) {
        this.price = price;
    }

    public List<ExpenditureTotal> getItems() {
        return items;
    }

    public void addItems(ExpenditureTotal expenditureTotal){
        items.add(expenditureTotal);
    }

    public void setItems(List<ExpenditureTotal> items) {
        this.items = items;
    }
}
